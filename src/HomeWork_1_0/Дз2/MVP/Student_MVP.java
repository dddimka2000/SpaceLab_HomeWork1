package HomeWork_1_0.Äç2.MVP;

public class Student_MVP {
    private String name;
    private int age;

    public Student_MVP(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }
}

